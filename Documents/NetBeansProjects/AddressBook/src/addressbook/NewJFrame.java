    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String searchName = jTextField6.getText();
        int flag=0;
        Collections.sort(addresses);
        for (int i = 0; i < addresses.size(); i++) {
            if (addresses.get(i).getName().equals(searchName)) {
                flag++;
                jTextArea1.setText(addresses.get(i).getName() + " " + addresses.get(i).getPhoneNumber() + " " + addresses.get(i).getEmail() + " " + addresses.get(i).getStreetAddress());
            }
        }
        if(flag==0){
            jTextArea1.setText("Not Found!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

