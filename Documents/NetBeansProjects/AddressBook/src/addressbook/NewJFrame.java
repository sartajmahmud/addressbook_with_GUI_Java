package addressbook;

import java.io.*;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Scanner;

class AddressBook implements Comparable<AddressBook> {

    private String name;
    private String phoneNumber;
    private String email;
    private String streetAddress;

    AddressBook(String name, String phoneNumber, String email, String streetAddress) {
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.streetAddress = streetAddress;
        writeFile();
    }

    private void writeFile() {
        try {
            File file = new File("addresses.txt");
            FileWriter fw = new FileWriter(file, true);
            fw.append("\n" + name + " " + phoneNumber + " " + email + " " + streetAddress);
            fw.close();
        } catch (IOException e) {

        }
    }

    @Override
    public int compareTo(AddressBook o) {
        return name.compareTo(o.name);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStreetAddress() {
        return streetAddress;
    }

    public void setStreetAddress(String streetAddress) {
        this.streetAddress = streetAddress;
    }

}

